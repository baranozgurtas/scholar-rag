"""Orchestrator: run retrieval ablation + RAGAS in one go.

Run with:
    python -m eval.run_full_eval
    make eval

Writes everything under eval/results/:
- ablation_<config>.json    (per-config raw)
- ablation_results.md       (4-config table)
- ragas_eval.json           (full pipeline records + RAGAS per-question)
- ragas_summary.md          (high-level RAGAS table)
- summary.md                (top-level combined report)
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path

from eval.ragas_eval import run_ragas_eval
from eval.retrieval_ablation import (
    QUESTIONS_PATH,
    RESULTS_DIR,
    load_questions,
    run_ablation,
    write_results,
)
from rag.logging_config import configure_logging, get_logger

logger = get_logger(__name__)


def _write_combined_summary(out_dir: Path) -> None:
    """Build a single top-level summary.md from the per-step outputs."""
    parts: list[str] = []
    parts.append("# Evaluation Summary\n")
    parts.append("Generated by `make eval` (orchestrator: `python -m eval.run_full_eval`).\n")
    parts.append("")

    ablation_md = out_dir / "ablation_results.md"
    if ablation_md.exists():
        parts.append("## Retrieval Ablation\n")
        parts.append(ablation_md.read_text().split("\n", 1)[-1] if ablation_md.exists() else "")
        parts.append("")

    ragas_md = out_dir / "ragas_summary.md"
    if ragas_md.exists():
        parts.append("## RAGAS\n")
        parts.append(ragas_md.read_text().split("\n", 1)[-1] if ragas_md.exists() else "")

    (out_dir / "summary.md").write_text("\n".join(parts) + "\n")
    logger.info("summary_written", path=str(out_dir / "summary.md"))


def run_full_eval(
    questions_path: Path = QUESTIONS_PATH,
    out_dir: Path = RESULTS_DIR,
    skip_ablation: bool = False,
    skip_ragas: bool = False,
) -> int:
    """Run the full eval suite."""
    out_dir.mkdir(parents=True, exist_ok=True)
    questions = load_questions(questions_path)
    if not questions:
        logger.error("no_questions_to_eval", path=str(questions_path))
        return 1

    logger.info("full_eval_start", n_questions=len(questions))

    if not skip_ablation:
        logger.info("ablation_phase_start")
        ablation_results = run_ablation(questions)
        write_results(ablation_results, out_dir=out_dir)
        logger.info("ablation_phase_done")
    else:
        logger.info("ablation_phase_skipped")

    if not skip_ragas:
        logger.info("ragas_phase_start")
        run_ragas_eval(questions_path=questions_path, out_dir=out_dir)
        logger.info("ragas_phase_done")
    else:
        logger.info("ragas_phase_skipped")

    _write_combined_summary(out_dir)
    logger.info("full_eval_done", out_dir=str(out_dir))
    return 0


def main(argv: list[str] | None = None) -> int:
    configure_logging()
    p = argparse.ArgumentParser(description="Run ablation + RAGAS end-to-end.")
    p.add_argument("--questions", type=Path, default=QUESTIONS_PATH)
    p.add_argument("--out-dir", type=Path, default=RESULTS_DIR)
    p.add_argument("--skip-ablation", action="store_true")
    p.add_argument("--skip-ragas", action="store_true")
    args = p.parse_args(argv)
    return run_full_eval(
        questions_path=args.questions,
        out_dir=args.out_dir,
        skip_ablation=args.skip_ablation,
        skip_ragas=args.skip_ragas,
    )


if __name__ == "__main__":
    sys.exit(main())


__all__ = ["main", "run_full_eval"]
